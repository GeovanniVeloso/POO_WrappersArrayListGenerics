package model;

public class Prova {

	private int numQuestoes;
	private float nota;

	public Prova() {
		super();
	}

	public int getNumQuestoes() {
		return numQuestoes;
	}

	public void setNumQuestoes(int numQuestoes) {
		this.numQuestoes = numQuestoes;
	}

	public float getNota() {
		return nota;
	}

	public void setNota(float nota) {
		this.nota = nota;
	}

}
